def build_texts(card_data: dict[str, Any]) -> dict[str, Any]:
    card_code = card_data["card_code"]
    name_ru = card_data["name_ru"]
    short_meaning, tags = MEANINGS[card_code]

    return {
        "deck_code": DECK_CODE,
        "card_code": card_code,
        "interpretation_set_code": INTERPRETATION_SET_CODE,
        "short_meaning": short_meaning,
        "general": f"{name_ru} указывает на {short_meaning}. Карта предлагает смотреть на ситуацию спокойно и выбирать шаг, который сохраняет внутреннюю ясность.",
        "love": f"В отношениях {name_ru} говорит о теме: {short_meaning}. Важны честность, бережный тон и готовность видеть не только ожидания, но и реальные действия.",
        "career": f"В работе {name_ru} подсвечивает {short_meaning}. Лучше опираться на факты, свой темп и понятный следующий шаг.",
        "money": f"В деньгах {name_ru} советует учитывать {short_meaning}. Не стоит действовать из тревоги; полезнее оценить ресурсы и риски спокойно.",
        "advice": f"Совет карты: заметьте, где сейчас проявляется {short_meaning}, и выберите один небольшой, но честный шаг.",
        "tags_jsonb": tags,
        "score": card_data["score"],
    }


CARD_INTERPRETATIONS = [build_texts(card_data) for card_data in TAROT_CARDS]


async def seed_card_interpretations() -> None:
    async with AsyncSessionLocal() as session:
        for interpretation in CARD_INTERPRETATIONS:
            statement = insert(CardInterpretation).values(**interpretation)
            update_values = {
                key: statement.excluded[key]
                for key in interpretation
                if key not in {"deck_code", "card_code", "interpretation_set_code"}
            }
            await session.execute(
                statement.on_conflict_do_update(
                    index_elements=[
                        CardInterpretation.deck_code,
                        CardInterpretation.card_code,
                        CardInterpretation.interpretation_set_code,
                    ],
                    set_=update_values,
                )
            )
        await session.commit()


def main() -> None:
    if len(CARD_INTERPRETATIONS) != 78:
        raise RuntimeError(f"Expected 78 interpretations, got {len(CARD_INTERPRETATIONS)}")
    asyncio.run(seed_card_interpretations())


if __name__ == "__main__":
    main()
